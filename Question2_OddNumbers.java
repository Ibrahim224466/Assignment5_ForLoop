package assignments.Assignment5_ForLoop;

public class Question2_OddNumbers {

	public static void main(String[] args) {
		
//Question-2
//Write a program that displays all odd numbers 
//between 3-130 in the same line	
		
		for (int odd=3; odd<=130; odd+=2) {
			System.out.print(odd+ ", ");
		}
		
		

	}

}
